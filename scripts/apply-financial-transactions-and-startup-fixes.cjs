#!/usr/bin/env node
/*
  REC Bot patch script
  - Adds Financials -> View Transactions wiring where the department reorg script can be detected.
  - Adds transaction pagination handlers for last 30 coin_transactions.
  - Patches the startup inventory backfill SQL bug in src/events/ready.ts.
  - Writes a Supabase SQL helper for missing payout_config.

  Run from project root:
    node apply-financial-transactions-and-startup-fixes.cjs
*/

const fs = require('fs');
const path = require('path');

const root = process.cwd();
function p(...parts) { return path.join(root, ...parts); }
function exists(file) { return fs.existsSync(file); }
function read(file) { return fs.readFileSync(file, 'utf8'); }
function write(file, content) { fs.writeFileSync(file, content, 'utf8'); }
function backup(file) {
  if (!exists(file)) return;
  const out = `${file}.bak-${new Date().toISOString().replace(/[:.]/g, '-')}`;
  fs.copyFileSync(file, out);
  console.log(`Backup: ${path.relative(root, out)}`);
}

const actionsHandlersPath = p('src', 'lib', 'actions-handlers.ts');
const readyPath = p('src', 'events', 'ready.ts');
const migrationsDir = p('sql');

function patchReady() {
  if (!exists(readyPath)) {
    console.warn('Skip ready.ts: file not found');
    return;
  }
  let s = read(readyPath);
  const before = s;

  // Fix invalid Postgres UPDATE ... FROM reference to target table in JOIN ON clause.
  // Replace: JOIN seasons s ON s.id = inventory.season_id AND s.guild_id = u.guild_id
  // With:    JOIN seasons s ON s.guild_id = u.guild_id
  //          WHERE ... AND s.id = inventory.season_id ...
  s = s.replace(
    /JOIN\s+seasons\s+s\s+ON\s+s\.id\s*=\s*inventory\.season_id\s+AND\s+s\.guild_id\s*=\s*u\.guild_id/g,
    'JOIN   seasons s ON s.guild_id = u.guild_id'
  );

  s = s.replace(
    /WHERE\s+inventory\.discord_id\s*=\s*u\.discord_id/g,
    'WHERE  inventory.discord_id      = u.discord_id\n        AND  s.id                      = inventory.season_id'
  );

  if (s !== before) {
    backup(readyPath);
    write(readyPath, s);
    console.log('Patched src/events/ready.ts startup inventory backfill SQL.');
  } else {
    console.log('No ready.ts backfill patch needed or pattern not found.');
  }
}

function ensureSqlHelper() {
  if (!exists(migrationsDir)) fs.mkdirSync(migrationsDir, { recursive: true });
  const file = path.join(migrationsDir, '2026-05-22_payout_config_and_transactions_helper.sql');
  if (exists(file)) {
    console.log(`SQL helper already exists: ${path.relative(root, file)}`);
    return;
  }
  const sql = `-- Run in Supabase SQL editor if payout_config is missing.\ncreate table if not exists payout_config (\n  key text primary key,\n  value integer not null default 0,\n  updated_at timestamp not null default now()\n);\n\n-- coin_transactions is used for Financials -> View Transactions.\n-- Only create it if your database does not already have it.\ncreate table if not exists coin_transactions (\n  id bigint generated by default as identity primary key,\n  guild_id text not null,\n  discord_id text not null,\n  amount integer not null,\n  type text not null,\n  description text not null,\n  related_user_id text,\n  created_at timestamp not null default now()\n);\n\ncreate index if not exists coin_transactions_user_created_idx\n  on coin_transactions (discord_id, created_at desc);\n`;
  write(file, sql);
  console.log(`Wrote SQL helper: ${path.relative(root, file)}`);
}

function patchActionsHandlers() {
  if (!exists(actionsHandlersPath)) {
    console.warn('Skip actions-handlers.ts: file not found');
    return;
  }
  let s = read(actionsHandlersPath);
  const before = s;

  // Ensure desc is imported from drizzle-orm if the local file import list was changed.
  if (s.includes('from "drizzle-orm"') && !/import\s*\{[^}]*\bdesc\b[^}]*\}\s*from\s*"drizzle-orm"/.test(s)) {
    s = s.replace(/import\s*\{([^}]*)\}\s*from\s*"drizzle-orm";/, (m, inner) => {
      return `import {${inner.trim()}, desc } from "drizzle-orm";`;
    });
  }

  // Add helper functions near session helpers if not already present.
  if (!s.includes('function transactionFlagSymbol')) {
    const marker = 'function cancelRow()';
    const insert = `\nfunction transactionFlagSymbol(tx: any): string {\n  const text = String(tx?.description ?? tx?.type ?? '').toLowerCase();\n  // These are conservative flags for anything that appears to require commissioner action.\n  if (text.includes('pending') || text.includes('approval') || text.includes('approve') || text.includes('applied') || text.includes('confirm')) return '⚠️';\n  return '✅';\n}\n\nfunction renderTxAmount(amount: number): string {\n  if (amount > 0) return "+" + amount;\n  return String(amount);\n}\n\nasync function buildTransactionHistoryPage(userId: string, guildId: string, page = 0) {\n  const pageSize = 10;\n  const maxRows = 30;\n  const safePage = Math.max(0, Math.min(2, page));\n\n  const rows = await db\n    .select({\n      id: coinTransactionsTable.id,\n      guildId: coinTransactionsTable.guildId,\n      amount: coinTransactionsTable.amount,\n      type: coinTransactionsTable.type,\n      description: coinTransactionsTable.description,\n      relatedUserId: coinTransactionsTable.relatedUserId,\n      createdAt: coinTransactionsTable.createdAt,\n    })\n    .from(coinTransactionsTable)\n    .where(eq(coinTransactionsTable.discordId, userId))\n    .orderBy(desc(coinTransactionsTable.createdAt))\n    .limit(maxRows);\n\n  const totalPages = Math.max(1, Math.ceil(rows.length / pageSize));\n  const pageRows = rows.slice(safePage * pageSize, safePage * pageSize + pageSize);\n\n  const description = pageRows.length\n    ? pageRows.map((tx, index) => {\n        const n = safePage * pageSize + index + 1;\n        const date = tx.createdAt ? new Date(tx.createdAt as any).toLocaleString('en-US', { timeZone: 'America/Chicago' }) : 'Unknown date';\n        const flag = transactionFlagSymbol(tx);\n        return '**' + n + '.** ' + flag + ' **' + renderTxAmount(Number(tx.amount ?? 0)) + '** — ' + String(tx.type) + '\\n' + String(tx.description ?? 'No description') + '\\n_' + date + ' CST_';\n      }).join('\\n\\n')\n    : 'No transactions found.';\n\n  const embed = new EmbedBuilder()\n    .setColor(0xB68B2D)\n    .setTitle('Financials: Transaction History')\n    .setDescription(description)\n    .setFooter({ text: 'Showing up to your last 30 transactions • Page ' + (safePage + 1) + '/' + totalPages + ' • ⚠️ pending/unconfirmed/applied-check item' });\n\n  const row = new ActionRowBuilder<ButtonBuilder>().addComponents(\n    new ButtonBuilder()\n      .setCustomId('ac_fin_tx_prev:' + safePage)\n      .setLabel('Back')\n      .setEmoji('⬅️')\n      .setStyle(ButtonStyle.Secondary)\n      .setDisabled(safePage <= 0),\n    new ButtonBuilder()\n      .setCustomId('ac_financials')\n      .setLabel('Financials')\n      .setStyle(ButtonStyle.Secondary),\n    new ButtonBuilder()\n      .setCustomId('ac_fin_tx_next:' + safePage)\n      .setLabel('Next')\n      .setEmoji('➡️')\n      .setStyle(ButtonStyle.Secondary)\n      .setDisabled(safePage >= totalPages - 1),\n  );\n\n  return { embed, row };\n}\n`;
    const idx = s.indexOf(marker);
    if (idx !== -1) {
      s = s.slice(0, idx) + insert + '\n' + s.slice(idx);
    } else {
      s += '\n' + insert + '\n';
    }
  }

  // Add select option into any Financials selector block, before Wager if present.
  if (!s.includes('value: "fin_transactions"') && !s.includes("value: 'fin_transactions'")) {
    const option = `\n          {\n            label: "View Transactions",\n            value: "fin_transactions",\n            description: "View your last 30 wallet, savings, store, wager, and transfer transactions",\n            emoji: "📜",\n          },`;

    // Prefer inserting before the Wager option in a financial selector.
    const wagerPattern = /(\n\s*\{\s*label:\s*["']Wager["'],\s*\n\s*value:\s*["'](?:wager|ac_wager|fin_wager)["'][\s\S]*?\n\s*\},)/;
    if (wagerPattern.test(s)) {
      s = s.replace(wagerPattern, option + '$1');
    } else {
      // Fallback: add before the closing .addOptions call near Financials text if detectable.
      const finIdx = s.indexOf('Financials');
      if (finIdx !== -1) {
        const addIdx = s.indexOf('.addOptions(', Math.max(0, finIdx - 1000));
        const closeIdx = addIdx === -1 ? -1 : s.indexOf('),', addIdx);
        if (closeIdx !== -1) {
          s = s.slice(0, closeIdx) + option + s.slice(closeIdx);
        } else {
          console.warn('Could not auto-insert View Transactions option. Add value fin_transactions manually to Financials selector.');
        }
      } else {
        console.warn('Could not locate Financials selector. Add View Transactions option manually.');
      }
    }
  }

  // Add handlers near top of exported interaction handler. We avoid assuming exact switch layout by injecting after guild/user id declarations if possible.
  if (!s.includes('ac_fin_transactions') && !s.includes('fin_transactions')) {
    // This branch probably impossible because we inserted option text above; kept for idempotence.
  }

  if (!s.includes('buildTransactionHistoryPage(i.user.id')) {
    const handlerBlock = `\n  // Financials -> View Transactions pagination\n  if (i.isStringSelectMenu?.() && i.customId === "ac_financials_select" && i.values?.[0] === "fin_transactions") {\n    const page = await buildTransactionHistoryPage(i.user.id, i.guildId!, 0);\n    await i.update({ embeds: [page.embed], components: [page.row] });\n    return;\n  }\n\n  if (i.isButton?.() && (i.customId.startsWith("ac_fin_tx_prev:") || i.customId.startsWith("ac_fin_tx_next:"))) {\n    const [kind, rawPage] = i.customId.split(":");\n    const current = Number(rawPage || 0);\n    const nextPage = kind === "ac_fin_tx_next" ? current + 1 : current - 1;\n    const page = await buildTransactionHistoryPage(i.user.id, i.guildId!, nextPage);\n    await i.update({ embeds: [page.embed], components: [page.row] });\n    return;\n  }\n`;

    // Try common function signature in this file: export async function handleActionsInteraction(i: AnyInteraction)
    const sig = /export\s+async\s+function\s+handleActionsInteraction\s*\(\s*i\s*:\s*AnyInteraction\s*\)\s*\{/;
    if (sig.test(s)) {
      s = s.replace(sig, (m) => m + handlerBlock);
    } else {
      // Try generic first async exported handler parameter named interaction.
      const sig2 = /export\s+async\s+function\s+handleActionsInteraction\s*\(\s*interaction\s*:\s*AnyInteraction\s*\)\s*\{/;
      if (sig2.test(s)) {
        const block2 = handlerBlock.replace(/\bi\./g, 'interaction.').replace(/\bi\)/g, 'interaction)').replace(/\bi,/g, 'interaction,');
        s = s.replace(sig2, (m) => m + block2);
      } else {
        console.warn('Could not auto-insert transaction handlers. Look for handleActionsInteraction and add handlers manually.');
      }
    }
  }

  if (s !== before) {
    backup(actionsHandlersPath);
    write(actionsHandlersPath, s);
    console.log('Patched src/lib/actions-handlers.ts for Financials -> View Transactions.');
  } else {
    console.log('No actions-handlers.ts changes made.');
  }
}

patchReady();
ensureSqlHelper();
patchActionsHandlers();
console.log('Done. Restart the bot with: npm run dev');
