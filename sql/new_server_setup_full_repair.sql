alter table if exists server_settings
  add column if not exists setup_completed boolean not null default false,
  add column if not exists setup_completed_at timestamp,
  add column if not exists setup_completed_by text,
  add column if not exists advance_hours integer,
  add column if not exists rules_text text;

create table if not exists new_server_setups (
  guild_id text primary key,
  started_by text,
  commissioner_ids text[] default '{}',
  rules_text text,
  season_number integer,
  current_week text,
  advance_hours integer,
  status text not null default 'not_started',
  completed_by text,
  completed_at timestamp,
  created_at timestamp not null default now(),
  updated_at timestamp not null default now()
);

create table if not exists league_commissioners (
  id bigint generated by default as identity primary key,
  guild_id text not null,
  discord_id text not null,
  role text not null default 'commissioner',
  created_by text,
  created_at timestamp not null default now(),
  unique(guild_id, discord_id)
);

create table if not exists league_members (
  id bigint generated by default as identity primary key,
  guild_id text not null,
  discord_id text not null,
  team_id bigint,
  role text not null default 'user',
  is_commissioner boolean not null default false,
  created_at timestamp not null default now(),
  updated_at timestamp not null default now(),
  unique(guild_id, discord_id)
);
